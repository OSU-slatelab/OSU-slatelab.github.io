// Some code to change highlighting based on current screen

var sethighlight = function(e) {
  var hash=location.hash == '' ? '#' : location.hash

  $('#nav a').each(function() {
    var link=$(this)
    link[ link.attr('href') === hash ? 'addClass' : 'removeClass' ]('selected')
  })

  var tag='main-'+hash.replace(/^\#/,'')
  $('main').each(function() {
    var section=$(this)
    section[ section.attr('id')===tag ? 'addClass': 'removeClass' ]('active')
  })
}

$( window ).on( 'hashchange', sethighlight);
$( window ).on( 'load', sethighlight )

// Turn an array into a set of cards

var cardify=function(studentarray,element,linkmsg='Link',linkmsg2='Google Scholar',aggregate=false) {
  var personcount=0
  var employerdict={}
  var locationdict={}

  studentarray.forEach(function(s) {
    var imgix=6
    if (aggregate) { imgix=8 }
    var interior='<div class="card"><div class="card-body">'
    if (s.length > imgix) {
      interior+='<img class="rounded-circle img-fluid" src="'+s[imgix]+'" alt="'+s[0]+'">' 
    }
    interior+='<h5 class="card-title">'+s[0]+'</h5><h6 class="card-subtitle mb-2 text-muted">'+s[1]+'</h6>'
    if (s[2]!='') { interior +='<div class="text-muted">'+s[2]+'</div>' }
    if (s[3]!='') { interior +='<div class="text-muted">'+s[3]+'</div>' }
    if (s[4]!='') { interior +='<a href="'+s[4]+'" class="card-link">'+linkmsg+'</a>' }
    if (s[5]!='') { interior +='<a href="'+s[5]+'" class="card-link">'+linkmsg2+'</a>' }
    interior +='</div></div>'
    $(element).append(interior)

    if (aggregate) {
      personcount+=1
      if (s[6]!="") {
        if (s[6] in employerdict) {
          employerdict[s[6]]+=1
        } else {
          employerdict[s[6]]=1
        }
      }
      if (s[7]!="") {
        if (s[7] in locationdict) {
          locationdict[s[7]]+=1
        } else {
          locationdict[s[7]]=1
        }
      }
      
    }
  })

  if (aggregate && personcount>0) {
      $('#numalumni').html(personcount)
      employ=Object.keys(employerdict).map(function(key) {
        return [key, employerdict[key]];
      });
      employ.sort(function(a,b) {
        return (a[1]==b[1])?(''+a[0]).localeCompare(b[0]):b[1]-a[1];
      })
      employstr='<div class="row no-gutters"><div class="col-4">'
      var ct=0
      var b1=Math.ceil(employ.length/3)
      var b2=Math.ceil(2*employ.length/3)
      employ.forEach(function(e) {
        ct+=1
        employstr+=e[0]
        if (e[1]>1) {
          employstr+=' ('+e[1]+')'
        }
        employstr+='<br/>'
        if (ct==b1 || ct==b2) {
          employstr+='</div><div class="col-4">'
        }
      });
      employstr+='</div></div>'
      $('#alumniemployers').html(employstr)
    }
}
